public class Task2_2 {
    public static void main(String[] args) {
        // Цикл while: от 1 до 10
        int i = 1;
        while (i <= 10) {
            System.out.print(i + " ");
            i++;
        }

        // Переход на новую строку
        System.out.println();

        // Цикл for: от 10 до 1
        for (int j = 10; j >= 1; j--) {
            System.out.print(j + " ");
        }
    }
}